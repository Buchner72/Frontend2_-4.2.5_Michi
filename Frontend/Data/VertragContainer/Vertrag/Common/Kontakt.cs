using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Vertrag
{
    public class Kontakt
    {
        #region Members Kontakt
        string _tel;
        string _mobil;
        string _fax;
        string _email;
        #endregion

        #region Property Kontakt
        public string Tel
        {
            get { return _tel; }
            set { _tel = value; }
        }
        public string Mobil
        {
            get { return _mobil; }
            set { _mobil = value; }
        }
        public string Fax
        {
            get { return _fax; }
            set { _fax = value; }
        }
        public string Email
        {
            get { return _email; }
            set { _email = value; }
        }
        #endregion

        #region Konstruktor Kontakt
        public Kontakt()
        {
            _tel = string.Empty;
            _mobil = string.Empty;
            _fax = string.Empty;
            _email = string.Empty;
        }
        #endregion
    }
}
